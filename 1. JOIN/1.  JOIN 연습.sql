/*
INNER JOIN
JOB_CODE 조인
*/
SELECT*
FROM JOB A
INNER JOIN EMPLOYEE B
ON A.JOB_CODE = B.JOB_CODE;


--LEFT JOIN
--LEFT JOIN 시 'LEFT TABLE'에는 없는 값까지 동시에 조회된다.
SELECT*
FROM JOB A
LEFT JOIN EMPLOYEE B
ON A.JOB_CODE = B.JOB_CODE;

---RIGHT JOIN
--LEFT JOIN과 반대로, 'RIGHT TABLE'에는 없는 값까지 동시에 조회한다.
SELECT *
FROM JOB A
RIGHT JOIN EMPLOYEE B
ON A.JOB_CODE = B.JOB_CODE;

--응용
SELECT JOB_NAME, EMP_NAME
FROM EMPLOYEE A
LEFT JOIN JOB B
ON A.JOB_CODE = B.JOB_CODE;

--FULL JOIN
SELECT *
FROM EMPLOYEE A
FULL JOIN JOB B
ON A.JOB_CODE = B.JOB_CODE;



--CROSS JOIN
--J1만 출력
SELECT *
FROM EMPLOYEE A
CROSS JOIN JOB B
WHERE A.JOB_CODE = 'J1';


--SELF JOIN
--FROM 절에 지기 자신의 TABLE을 입력.
SELECT *
FROM EMPLOYEE A,
     EMPLOYEE B
WHERE A.DEPT_CODE = 'D6';






